#include "libft.h"

int		ft_atoi(const char *str)
{
	int		res;
	int		sign;
	size_t	i;

	res = 0;
	sign = 1;
	i = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - 48);
		str++;
		i++;
	}
	if (i > 14)
		res = (sign < 0 ? 0 : -1);
	return (res * sign);
}
